package Day12;

public class StringTwo {
    public static void main(String[] args) {
        String s = "Kalyan";
        System.out.println(s.charAt(3));
        System.out.println(s.length());// To find the length
        System.out.println(s.indexOf("l"));// To find the index of the characters
        System.out.println(s.contains("Kal"));// To find the chaaracter/word is there or not
        System.out.println(s.endsWith("n"));// To find it is ends with
        System.out.println(s.toUpperCase());// It shifts the string to uppercase
        System.out.println(s.toLowerCase());// This shifts to lowercase
    }
}
