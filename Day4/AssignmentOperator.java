package Day4;

public class AssignmentOperator {
    public static void main(String[] args) {
        int a = 2;
        int b = 1;
        System.out.println(a==b); //false
        System.out.println(a+=b); //3
        System.out.println(a-=b); //1
        System.out.println(a*=b); //2
        System.out.println(a/=b); //2
    }   
}