package Day4;

public class RelactionalOperators {
    public static void main(String[] args) {
        int a = 22;
        int b = 11;
        System.out.println(a>b); //true
        System.out.println(a<b); //false
        System.out.println(a>=b); //true
        System.out.println(a<=b); //false
        System.out.println(a==b); //false
        System.out.println(a!=b); //true
    }   
}